import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const sourcePath = path.join(repoRoot, 'design/tokens/sdal.tokens.json');
const webOutPath = path.join(repoRoot, 'frontend-modern/src/generated/design-tokens.css');
const iosOutPath = path.join(repoRoot, 'ios-native/SDALNative/UI/Generated/SDALDesignTokens.generated.swift');
const androidOutPath = path.join(repoRoot, 'android-native/theme/SDALDesignTokens.kt');

const source = JSON.parse(fs.readFileSync(sourcePath, 'utf8'));
const light = source?.theme?.light || {};
const dark = source?.theme?.dark || {};
const radius = source?.layout?.radius || '16px';

function kebabToCamel(value) {
  return String(value || '').replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

function hexToAndroidColor(value) {
  const hex = String(value || '').trim();
  const match = hex.match(/^#([0-9a-fA-F]{6})$/);
  if (!match) return null;
  return `0xFF${match[1].toUpperCase()}`;
}

const cssLines = [
  '/* Auto-generated by scripts/sync-design-tokens.mjs. Do not edit directly. */',
  ':root {'
];

for (const [key, val] of Object.entries(light)) {
  cssLines.push(`  --${key}: ${val};`);
}
cssLines.push(`  --radius: ${radius};`);
cssLines.push('}');
cssLines.push('');
cssLines.push(":root[data-theme='dark'] {");
for (const [key, val] of Object.entries(dark)) {
  cssLines.push(`  --${key}: ${val};`);
}
cssLines.push('}');
cssLines.push('');

const swiftLines = [
  '// Auto-generated by scripts/sync-design-tokens.mjs. Do not edit directly.',
  'import Foundation',
  '',
  'enum SDALDesignTokens {',
  '    enum Light {',
  ...Object.entries(light).map(([key, val]) => `        static let ${kebabToCamel(key)} = "${val}"`),
  '    }',
  '',
  '    enum Dark {',
  ...Object.entries(dark).map(([key, val]) => `        static let ${kebabToCamel(key)} = "${val}"`),
  '    }',
  '',
  `    static let radius = "${radius}"`,
  '}'
];

const androidLines = [
  '// Auto-generated by scripts/sync-design-tokens.mjs. Do not edit directly.',
  'package theme',
  '',
  'object SDALDesignTokens {',
  ...Object.entries(light).map(([key, val]) => {
    const parsed = hexToAndroidColor(val);
    if (!parsed) return `    const val LIGHT_${key.replace(/-/g, '_').toUpperCase()} = "${val}"`;
    return `    const val LIGHT_${key.replace(/-/g, '_').toUpperCase()} = ${parsed}`;
  }),
  ...Object.entries(dark).map(([key, val]) => {
    const parsed = hexToAndroidColor(val);
    if (!parsed) return `    const val DARK_${key.replace(/-/g, '_').toUpperCase()} = "${val}"`;
    return `    const val DARK_${key.replace(/-/g, '_').toUpperCase()} = ${parsed}`;
  }),
  `    const val RADIUS = "${radius}"`,
  '}'
];

fs.mkdirSync(path.dirname(webOutPath), { recursive: true });
fs.mkdirSync(path.dirname(iosOutPath), { recursive: true });
fs.mkdirSync(path.dirname(androidOutPath), { recursive: true });

fs.writeFileSync(webOutPath, `${cssLines.join('\n')}\n`, 'utf8');
fs.writeFileSync(iosOutPath, `${swiftLines.join('\n')}\n`, 'utf8');
fs.writeFileSync(androidOutPath, `${androidLines.join('\n')}\n`, 'utf8');

console.log('Design tokens synced:');
console.log(`- ${path.relative(repoRoot, webOutPath)}`);
console.log(`- ${path.relative(repoRoot, iosOutPath)}`);
console.log(`- ${path.relative(repoRoot, androidOutPath)}`);
